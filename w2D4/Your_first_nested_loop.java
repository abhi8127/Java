package w2D4;

public class Your_first_nested_loop {
    public static void main(String[] args){
        int n=5;
        for(int i=1;i<=n;i++)
        {
            for(int j=1;j<=n;j++)
            {
                System.out.print(j + " ");
            }
            System.out.println();

        }
    }
}
